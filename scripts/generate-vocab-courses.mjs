// GENERATE SQL — vocab entries (UPSERT for idempotency)
// ══════════════════════════════════════════════════════════════════════════

function escSQL(s) { return (s || '').replace(/'/g, "''") }
function arrSQL(arr) {
  if (!arr || arr.length === 0) return "'{}'"
  return `ARRAY[${arr.map(s => `'${escSQL(s)}'`).join(', ')}]`
}

let sql = `-- =============================================================================
-- 15 — Seed vocabulary: ${VOCAB_DATA.length} word entries for vocabulary courses
--
-- Uses INSERT ... ON CONFLICT DO UPDATE so existing entries (from earlier
-- migrations) are updated with the latest data while new entries are created.
-- =============================================================================

`

for (const v of VOCAB_DATA) {
  sql += `INSERT INTO public.vocab (id, word, pos, definition_en, definition_zh, example_en, example_zh, pixel_icon, synonyms, minecraft_role, minecraft_obtain)
VALUES (
  '${escSQL(v.id)}', '${escSQL(v.word)}', '${escSQL(v.pos)}',
  '${escSQL(v.definition_en)}', '${escSQL(v.definition_zh)}',
  '${escSQL(v.example_en)}', '${escSQL(v.example_zh)}',
  '${escSQL(v.pixel_icon)}', ${arrSQL(v.synonyms)},
  '${escSQL(v.minecraft_role)}', '${escSQL(v.minecraft_obtain)}'
)
ON CONFLICT (id) DO UPDATE SET
  definition_en = EXCLUDED.definition_en,
  definition_zh = EXCLUDED.definition_zh,
  example_en = EXCLUDED.example_en,
  example_zh = EXCLUDED.example_zh,
  pixel_icon = EXCLUDED.pixel_icon,
  synonyms = EXCLUDED.synonyms,
  minecraft_role = EXCLUDED.minecraft_role,
  minecraft_obtain = EXCLUDED.minecraft_obtain;

`
}

fs.writeFileSync(path.join(SQL_DIR, '15_seed_vocab_courses.sql'), sql)
